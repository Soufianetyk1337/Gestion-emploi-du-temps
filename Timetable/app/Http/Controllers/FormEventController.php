     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $form = new FormEvents();
        $form->seance=$request->get('seance');
        $form->professeur=$request->get('professeur');
        $form->dateSeance=$request->get('dateSeance');
        $form->debutSeance=$request->get('debutSeance');
        $form->finSeance=$request->get('finSeance');
        $form->save();
        return response()->json('Successfully added');
    }

